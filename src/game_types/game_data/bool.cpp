#include "bool.h"

using namespace mafia::game_types::game_data;

uintptr_t Bool::type_def {0};
uintptr_t Bool::data_type_def {0};
mafia::game_types::RVPoolAllocator* Bool::pool_alloc_base {nullptr};

Bool::Bool()
{
    *reinterpret_cast<uintptr_t*>(this) = type_def;
    *reinterpret_cast<uintptr_t*>(static_cast<mafia::game_types::DebugValue*>(this)) = data_type_def;
    val = false;
}

Bool::Bool(bool val_)
{
    *reinterpret_cast<uintptr_t*>(this) = type_def;
    *reinterpret_cast<uintptr_t*>(static_cast<mafia::game_types::DebugValue*>(this)) = data_type_def;
    val = val_;
}

Bool::Bool(const Bool& copy_)
{
    *reinterpret_cast<uintptr_t*>(this) = type_def;
    *reinterpret_cast<uintptr_t*>(static_cast<mafia::game_types::DebugValue*>(this)) = data_type_def;
    val = copy_.val;
}

Bool::Bool(Bool&& move_) noexcept
{
    *reinterpret_cast<uintptr_t*>(this) = type_def;
    *reinterpret_cast<uintptr_t*>(static_cast<mafia::game_types::DebugValue*>(this)) = data_type_def;
    _refcount = move_._refcount;
    val = move_.val;
}

Bool& Bool::operator=(const Bool& copy_)
{
    *reinterpret_cast<uintptr_t*>(this) = type_def;
    *reinterpret_cast<uintptr_t*>(static_cast<mafia::game_types::DebugValue*>(this)) = data_type_def;
    val = copy_.val;
    return *this;
}

Bool& Bool::operator=(Bool&& move_) noexcept
{
    *reinterpret_cast<uintptr_t*>(this) = type_def;
    *reinterpret_cast<uintptr_t*>(static_cast<mafia::game_types::DebugValue*>(this)) = data_type_def;
    _refcount = move_._refcount;
    val = move_.val;
    return *this;
}

void* Bool::operator new(std::size_t)
{
    return pool_alloc_base->allocate(1);
}

void Bool::operator delete(void* ptr_, std::size_t)
{
    return pool_alloc_base->deallocate(ptr_);
}
